/**
 * Setup - Environment Configuration
 *
 * A settings page for configuring the .env file
 * with individually saveable sections.
 *
 * Refactored from 1,717 lines to ~350 lines by extracting:
 * - Form components to components/setup/FormComponents.tsx
 * - Section definitions to components/setup/setupConstants.ts
 * - Individual sections to components/setup/sections/
 */

import { useState, useCallback, useEffect } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import { useMutation, useQuery } from '@tanstack/react-query';
import { clsx } from 'clsx';
import toast from 'react-hot-toast';
import { Card } from '../components/Card';
import { api } from '../api/client';
import type { EnvironmentConfig } from '../types/setup';
import { defaultEnvironmentConfig } from '../types/setup';

// Import extracted components
import {
  LoadingSpinner,
  SECTIONS,
  LocationSyncPanel,
  EnvironmentSection,
  BackendDatabaseSection,
  XSightDatabaseSection,
  MobiControlDatabaseSection,
  LLMSection,
  MobiControlAPISection,
  StreamingSection,
  SecuritySection,
} from '../components/setup';

